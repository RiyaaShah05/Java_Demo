
public class SwitchCase {
	public static void main(String arg[]) {
		int num = 1;
		
		switch(num)
		{
		case 1:
			System.out.println("One");
			break;
		case 2:
			System.out.println("Two");
			break;
		case 3:
			System.out.println("Three");
			break;
		default:
			System.out.println("No match");
		}
		
		
		
		
	}
	

}
